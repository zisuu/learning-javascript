// Declare a variable
let x;
console.log(x);       // >> undefined

// Assign a value to a variable
x = 12;
console.log(x);      // >> 12

// Declare and assign in one stroke
let y = 4;

// Variables declared using 'let' can be reassigned
y = 6;

// Variables declared using 'const' cannot be reassigned
const z = 7;
z = 13;   // ERROR
